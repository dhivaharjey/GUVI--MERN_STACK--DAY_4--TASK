const titleCaps = (str) => {
  let strg = str.toLowerCase().split(" ");
  for (let i = 0; i < strg.length; i++) {
    strg[i] = strg[i].charAt(0).toUpperCase() + strg[i].slice(1);
  }
  return strg.join(" ");
};

