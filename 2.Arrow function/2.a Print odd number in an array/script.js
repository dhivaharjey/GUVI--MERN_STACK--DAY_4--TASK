const oddNumber = (arr) => {
  let odd = [];
  for (let val of arr) {
    if (val % 2 !== 0) {
      odd.push(val);
    }
  }
  return odd;
};

const array = [12, 45, 22, 46, 78, 2, 13, 11, 19, 23];
