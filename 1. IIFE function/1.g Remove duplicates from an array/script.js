const removeDuplicate = (function remove(arr) {
  let originalArray = [];
  for (let val of arr) {
    if (arr.indexOf(val) == arr.lastIndexOf(val)) {
      originalArray.push(val);
    }
  }

  return originalArray;
})([2, 3, 1, 4, 5, 6, 7, 3, 4, 5, 68, 8, 9, 23, 4, 5, 11, 35, 67]);

